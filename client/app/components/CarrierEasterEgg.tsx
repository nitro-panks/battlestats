const WIDTH = 900;
const HEIGHT = 300;
const CELL_W = WIDTH / COLS; // 6px
const CELL_H = HEIGHT / ROWS; // 10px
const BG = '#05080d';
const PHOSPHOR = '226, 235, 247'; // cool white, rgb triplet

// Deterministic 0/1 per cell so the binary field is stable across renders
// (no Math.random — keeps the art identical run to run) yet scattered enough
// to read as code rather than a regular checkerboard.
const bit = (x: number, y: number): string =>
    ((x * 374761393 + y * 668265263) ^ ((x * y) << 3)) & 1 ? '1' : '0';

const CarrierEasterEgg: React.FC = () => {
    const ref = useRef<HTMLCanvasElement | null>(null);

    useEffect(() => {
        const canvas = ref.current;
        if (!canvas) return;
        const ctx = canvas.getContext('2d');
        if (!ctx) return;

        const dpr = typeof window !== 'undefined' ? Math.min(window.devicePixelRatio || 1, 2) : 1;
        canvas.width = WIDTH * dpr;
        canvas.height = HEIGHT * dpr;
        ctx.setTransform(dpr, 0, 0, dpr, 0, 0);

        ctx.fillStyle = BG;
        ctx.fillRect(0, 0, WIDTH, HEIGHT);

        ctx.font = `${Math.round(CELL_H * 0.9)}px ui-monospace, SFMono-Regular, Menlo, Consolas, monospace`;
        ctx.textBaseline = 'middle';
        ctx.textAlign = 'center';

        for (let y = 0; y < ROWS; y++) {
            const row = LUM[y];
            for (let x = 0; x < COLS; x++) {
                const lum = parseInt(row[x], 16) / 15;
                // Faint baseline so dark areas keep a low hum of code (like the
                // reference), bright pixels (deck/wake) reach full glow.
                const alpha = Math.max(0.04, Math.pow(lum, 1.1));
                ctx.fillStyle = `rgba(${PHOSPHOR}, ${alpha.toFixed(3)})`;
                ctx.fillText(bit(x, y), x * CELL_W + CELL_W / 2, y * CELL_H + CELL_H / 2);
            }
        }
    }, []);

    return (
        <canvas
            ref={ref}
            role="img"
            aria-label="There are no Tier 9 aircraft carriers — but here is one rendered in binary."
            className="block w-full rounded-lg"
            style={{ maxWidth: WIDTH, aspectRatio: `${WIDTH} / ${HEIGHT}`, background: BG }}
        />
    );
};

export default CarrierEasterEgg;
